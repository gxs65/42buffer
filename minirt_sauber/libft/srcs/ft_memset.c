#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	char		*str;
	size_t		i;

	str = (char *)s;
	i = 0;
	while (i < len)
	{
		str[i] = c;
		i++;
	}
	return (s);
}
